package classes;

public class Position {
	
	private final int xPosition;
	
	private final int yPosition;
	
	Position(int x, int y) {
		this.xPosition = x;
		this.yPosition = y;
	}
	
	public int getX() {
		return xPosition;
	}

	public int getY() {
		return yPosition;
	}


	
}
